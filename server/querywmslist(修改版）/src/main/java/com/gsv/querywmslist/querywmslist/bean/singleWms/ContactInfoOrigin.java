package com.gsv.querywmslist.querywmslist.bean.singleWms;

public class ContactInfoOrigin {
    private String organization;
    private String person;
    private String position;
    private String address;
    private String city;
    private String stateOrProvince;
    private String country;
    private String postCode;
    private String voiceTelephone;
    private String facsimileTelephone;
    private String electronicMailAddress;

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public String getPerson() {
        return person;
    }

    public void setPerson(String person) {
        this.person = person;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getStateOrProvince() {
        return stateOrProvince;
    }

    public void setStateOrProvince(String stateOrProvince) {
        this.stateOrProvince = stateOrProvince;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getVoiceTelephone() {
        return voiceTelephone;
    }

    public void setVoiceTelephone(String voiceTelephone) {
        this.voiceTelephone = voiceTelephone;
    }

    public String getFacsimileTelephone() {
        return facsimileTelephone;
    }

    public void setFacsimileTelephone(String facsimileTelephone) {
        this.facsimileTelephone = facsimileTelephone;
    }

    public String getElectronicMailAddress() {
        return electronicMailAddress;
    }

    public void setElectronicMailAddress(String electronicMailAddress) {
        this.electronicMailAddress = electronicMailAddress;
    }
}
